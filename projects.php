			  					<div class="bottom-margin">
				  					<b>Project:</b> Phase Amplitude Coupled comodulogram maps in EEG and ECoG data<br>
									<b>Mentor:</b> Diana Escalona-Vargas<br>
								</div>
								<div>
									<P><b>Description:</b> Studies have shown that brain functions are achieved with
									simultaneous oscillations in different frequency bands. This interaction between
									oscillations is also known as cross-frequency coupling (CFC) and can be useful in
									understanding brain function. Phase amplitude coupling (PAC) is when the phase of the
									lower frequency oscillation drives the power of the coupled higher frequency oscillation.
									Electrophysiological studies in humans and animals have shown a role of oscillatory
									activity and cross-frequency interactions for both motor control and memory functions.
									The coupling map, also known as comodulogram, shows PAC in the frequency domain
									and indicates the level of coupling between a pair of low and high frequencies. The
									objective of this research project is to develop a methodology for representing the PAC
									comodulogram maps across sensors based on the maximum coupling intensity for EEG
									and ECoG data. The students will learn the pre-processing techniques for artifacts
									removal and calculation of the PAC maps. Then, each map will be normalized and a
									clustering technique will use to represent a single comodulogram across sensors.</P>
